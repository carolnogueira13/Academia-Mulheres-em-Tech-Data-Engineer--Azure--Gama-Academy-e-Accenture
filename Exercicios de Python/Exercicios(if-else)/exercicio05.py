num1 = int(input("Digite um numero de (1 a 7) para saber o dia da semana correspondente: "))

if num1 == 1:
    print("Domingo")
elif num1 == 2:
    print("Segunda")
elif num1 == 3:
    print("Terça")
elif num1 == 4:
    print("Quarta")
elif num1 == 5:
    print("Quinta")
elif num1 == 6:
    print("Sexta")
elif num1 == 7:
    print("Sábado")
else:
    print("Valor inválido")


